namespace MqoImporter
{
    /// <summary>
    /// XYを持つベクタ
    /// </summary>
    public struct Vector2
    {
        public float X = 0;
        public float Y = 0;
    }

    /// <summary>
    /// XYZを持つベクタ
    /// </summary>
    public struct Vector3
    {
        public float X = 0;
        public float Y = 0;
        public float Z = 0;
    }

    /// <summary>
    /// XYZWを持つベクタ
    /// </summary>
    public struct Vector4
    {
        public float X = 0;
        public float Y = 0;
        public float Z = 0;
        public float W = 0;

    }
}
